#  -----------instruction of sum-----------
# in C a simple sum code: a = b + c;
# in Assembly 

add a, b, c

# 'add' is the operator 
# 'b,c' is the valors to use in operation
# 'a' is the destination of the result of the operation

# -----------instruction of sub-----------
# inc C a simple code of a subtration is d = e - f;
# in Assembly we have

sub d, e, f

# 'sub' is the operator, and follow the same logic of the 'sum' parameters